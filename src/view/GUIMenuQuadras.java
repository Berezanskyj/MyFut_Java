    private void preencherTabela(){
        try{
            QuadraServicos qs = ServicosFactory.getQuadraServicos();
            
            ArrayList<QuadraVO> reg = new ArrayList<>();
            
            reg = qs.buscarQuadra();
            
            for (int i = 0; i < reg.size(); i++){
                dtm.addRow(new String[] {
                    String.valueOf(reg.get(i).getIdquadra()),
                    String.valueOf(reg.get(i).getNome_responsavel()),
                    String.valueOf(reg.get(i).getTelefone_proprietario()),
                    String.valueOf(reg.get(i).getEndereco()),
                    String.valueOf(reg.get(i).getCnpj_responsavel()),
                    String.valueOf(reg.get(i).getMax_jogadores()),
                    String.valueOf(reg.get(i).getNumero_quadra()),
                    String.valueOf(reg.get(i).getNomeEstabelecimento())
                                });
            }//end of for
            
            jtQuadra.setModel(dtm);
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(
                    null,
                    "Erro! GUIManuQuadra.preencherTabela" + e.getMessage());
        }
    } // end of preencher
    
