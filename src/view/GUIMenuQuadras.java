    private void deletarQuadra(){
        try{
            int linha = jtQuadra.getSelectedRow();
            
            if(linha == -1) {
                JOptionPane.showMessageDialog(
                    null,
                    "Por favor selecione uma linha!");
            }else{
                QuadraServicos qs = ServicosFactory.getQuadraServicos();
                String idquadra = (String) jtQuadra.getValueAt(linha, 0);
                qs.deletarQuadra(Integer.parseInt(idquadra));

            } 
        } catch (Exception e){
            JOptionPane.showMessageDialog(
                null,
                "Erro ao deletar Quadra! " + e.getMessage());
        }
    } // end of filtrar
    
    private void filtrar(){
        try{
            if(jtfPesquisa.getText().isEmpty() ){
                preencherTabela();
            } else {
                QuadraServicos qs = ServicosFactory.getQuadraServicos();
                String pesquisa = (String) jcPesquisa.getSelectedItem();
                
                String query = null;
                
                if(pesquisa.equals("ID")){
                    query = "where idquadra = " + jtfPesquisa.getText();
                } else if (pesquisa.equals("Nome")){
                    query = "where nome_proprietario like '%" + jtfPesquisa.getText() + "%' ";
                } else if (pesquisa.equals("Telefone")){
                    query = "where telefone_proprietario like '%" + jtfPesquisa.getText() + "%' ";
                } else if (pesquisa.equals("Endereço")){
                    query = "where endereco_quadra like '%" + jtfPesquisa.getText() + "%' ";
                } else if (pesquisa.equals("CNPJ")){
                    query = "where cnpj_proprietario like '%" + jtfPesquisa.getText() + "%' ";
                } else if (pesquisa.equals("Estabelecimento")){
                    query = "where nome_estabelecimento like '%" + jtfPesquisa.getText() + "%' ";
                }
                
                ArrayList<QuadraVO> reg = new ArrayList<>();
                
                reg = qs.filtrarQuadra(query);
                
                for ( int i = 0; i < reg.size(); i++){
                    dtm.addRow(new String[]{
                        String.valueOf(reg.get(i).getIdquadra()),
                        String.valueOf(reg.get(i).getNome_responsavel()),
                        String.valueOf(reg.get(i).getTelefone_proprietario()),
                        String.valueOf(reg.get(i).getEndereco()),
                        String.valueOf(reg.get(i).getCnpj_responsavel()),
                        String.valueOf(reg.get(i).getMax_jogadores()),
                        String.valueOf(reg.get(i).getNumero_quadra()),
                        String.valueOf(reg.get(i).getNomeEstabelecimento())
                    });
                }
                
                jtQuadra.setModel(dtm);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(
                    null,
                    "Erro ao filtrar quadra! GUIMenuQuadra.Filtrar" + e.getMessage());
        }
    }
    
    
    
    private void alterarQuadra(){
        int linha = jtQuadra.getSelectedRow();
        if(linha != -1){

            jtfQuadraID.setText((String) jtQuadra.getValueAt(linha, 0));
            jtfNome.setText((String) jtQuadra.getValueAt(linha, 1));
            jtfTelefone.setText((String) jtQuadra.getValueAt(linha, 2));
            jtfEndereco.setText((String) jtQuadra.getValueAt(linha, 3));
            jtfCNPJ.setText((String) jtQuadra.getValueAt(linha, 4));
            jtfMaxJogadores.setText((String) jtQuadra.getValueAt(linha, 5));
            jtfQuadras.setText((String) jtQuadra.getValueAt(linha, 6));
            jtfEstabelecimento.setText((String) jtQuadra.getValueAt(linha, 7));
        }else{
            JOptionPane.showMessageDialog(
                null,
                "Você não selecionou nenhuma linha!");
        }
    }
    
    public void confirmarAlteracao(){
        try{
            QuadraVO qVO = new QuadraVO();

                                   
            if(jtfNome.getText().isEmpty() ||
                    jtfTelefone.getText().isEmpty() ||
                    jtfEndereco.getText().isEmpty() ||
                    jtfCNPJ.getText().isEmpty() ||
                    jtfMaxJogadores.getText().isEmpty() ||
                    jtfQuadras.getText().isEmpty() ||
                    jtfEstabelecimento.getText().isEmpty()
                    ) {
                    JOptionPane.showMessageDialog(
                    null,
                    "Erro! Por favor insira as informacoes corretamente!",
                    "Erro",
                    JOptionPane.ERROR_MESSAGE);
            } else{
                qVO.setIdquadra(Integer.parseInt(jtfQuadraID.getText()));
                qVO.setNome_responsavel(jtfNome.getText());
                qVO.setTelefone_proprietario(jtfTelefone.getText());
                qVO.setEndereco(jtfEndereco.getText());
                qVO.setCnpj_responsavel(jtfCNPJ.getText());
                qVO.setMax_jogadores(Integer.parseInt(jtfMaxJogadores.getText()));
                qVO.setNumero_quadra(Integer.parseInt(jtfQuadras.getText()));
                qVO.setNomeEstabelecimento(jtfEstabelecimento.getText());
                                
                QuadraServicos qs = ServicosFactory.getQuadraServicos();
                qs.alterarQuadra(qVO);
                
                JOptionPane.showMessageDialog(
                    null,
                    "Registro alterado com sucesso!");
                
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(
                    null,
                    "Erro!" + e.getMessage());
        }
    }
    
    
    
    
    
    
    
    
