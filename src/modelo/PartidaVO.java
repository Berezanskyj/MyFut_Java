public class PartidaVO {
    
    private int idpartida;
    private String nome_responsavel;
    private String telefone_responsavel;
    private int idquadra;
    private int num_quadra;
    private int max_jogadores;
    private String endereco_quadra;

    public int getIdpartida() {
        return idpartida;
    }

    public void setIdpartida(int idpartida) {
        this.idpartida = idpartida;
    }

    public String getNome_responsavel() {
        return nome_responsavel;
    }

    public void setNome_responsavel(String nome_responsavel) {
        this.nome_responsavel = nome_responsavel;
    }

    public String getTelefone_responsavel() {
        return telefone_responsavel;
    }

    public void setTelefone_responsavel(String telefone_responsavel) {
        this.telefone_responsavel = telefone_responsavel;
    }

    public int getIdquadra() {
        return idquadra;
    }

    public void setIdquadra(int idquadra) {
        this.idquadra = idquadra;
    }

    public int getNum_quadra() {
        return num_quadra;
    }

    public void setNum_quadra(int num_quadra) {
        this.num_quadra = num_quadra;
    }

    public int getMax_jogadores() {
        return max_jogadores;
    }

    public void setMax_jogadores(int max_jogadores) {
        this.max_jogadores = max_jogadores;
    }

    public String getEndereco_quadra() {
        return endereco_quadra;
    }

    public void setEndereco_quadra(String endereco_quadra) {
        this.endereco_quadra = endereco_quadra;
    }
    
    
    
    
    
    
    
}
